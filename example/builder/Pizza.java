package builder;

public class Pizza {
    private String dough;
    private String base;
    private String topping;

    public void setDough(final String dough) {
        this.dough = dough;
    }

    public void setBase(final String base) {
        this.base = base;
    }

    public void setTopping(final String topping) {
        this.topping = topping;
    }
}
